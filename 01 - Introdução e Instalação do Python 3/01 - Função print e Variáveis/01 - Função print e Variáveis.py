# Print something in the console
print("Hello, World!")

# The "sep" separates the arguments with the value that was set in it
print("Hello", "World!", sep="-")

# The "end" ends the line with the value that was set in it
print("Hello, World!", end="\n===========\n")

state = "Brazil"
# The "type()" returns the type of the variable
print(type(state))
